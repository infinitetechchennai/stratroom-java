import { useState, useEffect, useCallback } from 'react'
import { useAuth } from '../../context/AuthContext'
import axiosClient from '../../api/axiosClient'
import styles from './Controlpanel.module.css'

const TABS = [
  { key: 'general', label: 'General', icon: <SettingsIcon /> },
  { key: 'theme', label: 'Theme', icon: <ThemeIcon /> },
  { key: 'email', label: 'Email', icon: <EmailIcon /> },
  { key: 'sso', label: 'SSO', icon: <LockIcon /> },
  { key: 'etl', label: 'ETL', icon: <DatabaseIcon /> },
  { key: 'masters', label: 'Masters', icon: <ListIcon /> },
  { key: 'language', label: 'Language', icon: <GlobeIcon /> },
]

export default function Controlpanel() {
  const { user } = useAuth()
  const [activeTab, setActiveTab] = useState('general')
  const [tabData, setTabData] = useState(null)
  const [loading, setLoading] = useState(false)

  const loadTabData = useCallback(async (tab) => {
    setLoading(true)
    try {
      const res = await axiosClient.get(`/stratroom/controlPanel/${tab}`)
      setTabData(res.data)
    } catch {
      setTabData(null)
    } finally {
      setLoading(false)
    }
  }, [])

  useEffect(() => { loadTabData(activeTab) }, [activeTab, loadTabData])

  const handleTabChange = (tab) => {
    setActiveTab(tab)
  }

  return (
    <div className={styles.page}>
      <div className={styles.header}>
        <h4 className={styles.title}>
          <span className={styles.icon}>
            <ControlPanelIcon />
          </span>
          Control Panel
        </h4>
      </div>

      <div className={styles.card}>
        <div className={styles.layout}>
          {/* Sidebar tabs - matches controlpanel.jsp nav pills */}
          <div className={styles.sidebar}>
            {TABS.map(({ key, label, icon }) => (
              <button
                key={key}
                className={`${styles.tabBtn} ${activeTab === key ? styles.tabBtnActive : ''}`}
                onClick={() => handleTabChange(key)}
              >
                <span className={styles.tabIcon}>{icon}</span>
                <span className={styles.tabLabel}>{label}</span>
              </button>
            ))}
          </div>

          {/* Content area */}
          <div className={styles.tabContent}>
            {loading ? (
              <div className={styles.loader}>
                <div className={styles.spinner} />
              </div>
            ) : (
              <TabContent tab={activeTab} data={tabData} user={user} />
            )}
          </div>
        </div>
      </div>
    </div>
  )
}

function TabContent({ tab, data, user }) {
  const tabTitles = {
    general: 'General Settings',
    theme: 'Theme Customization',
    email: 'Email Configuration',
    sso: 'Single Sign-On',
    etl: 'ETL Configuration',
    masters: 'Masters Management',
    language: 'Language Settings',
  }

  return (
    <div className={styles.tabPanel}>
      <h5 className={styles.tabTitle}>{tabTitles[tab]}</h5>
      <div className={styles.tabPlaceholder}>
        <p>{tabTitles[tab]} content will be loaded here. This corresponds to the "{tab}" tab in the JSP Control Panel.</p>
      </div>
    </div>
  )
}

